numero: int = int(input("Dame un numero: "))

if numero < 0:
    print("El numero ", numero, "es negativo")
elif numero < 0:
    print("el numero ", numero, " es positivo")
else:
    print("el numero ", numero, " es 0")

if numero % 2 == 0:
    print("el numero ", numero, " es par")
else:
    print("el numero ", numero, " es impar")
